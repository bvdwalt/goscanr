func TopPorts(n int) ([]int, error) {
	if n < 1 || n > len(topPorts) {
		return nil, fmt.Errorf("top ports must be between 1 and %d", len(topPorts))
	}
	return topPorts[:n], nil
}

func PortRange(start, end int) []int {
	ports := make([]int, end-start+1)
	for i := range ports {
		ports[i] = start + i
	}
	return ports
}
